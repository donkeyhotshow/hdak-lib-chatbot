// ─── Matcher ───────────────────────────────────────────────────────────────

export function getQuickResponse(query: string): QuickResponse | null {
  const q = query.toLowerCase()

  if (q.includes('графік') || q.includes('розклад') || q.includes('години') ||
      q.includes('работа') || q.includes('schedule'))
    return QUICK_RESPONSES['Графік роботи бібліотеки']

  if (q.includes('записа') || q.includes('квиток') || q.includes('реєстра') ||
      q.includes('запись') || q.includes('register'))
    return QUICK_RESPONSES['Як записатися до бібліотеки']

  if (q.includes('каталог') || q.includes('пошук книг') || q.includes('знайти книг') ||
      q.includes('catalog') || q.includes('search'))
    return QUICK_RESPONSES['Електронний каталог']

  if (q.includes('контакт') || q.includes('адрес') || q.includes('телефон') ||
      q.includes('contact') || q.includes('де знаход'))
    return QUICK_RESPONSES['Контакти та адреса']

  if (q.includes('правил') || q.includes('rule'))
    return QUICK_RESPONSES['Правила бібліотеки']

  if (q.includes('е-читальна') || q.includes('електронна зала') || q.includes('комп\'ютер'))
    return QUICK_RESPONSES['Е-читальна зала']

  if (q.includes('репозитар') || q.includes('підручник') || q.includes('repository'))
    return QUICK_RESPONSES['Репозитарій ХДАК']

  if (q.includes('наук') || q.includes('scopus') || q.includes('wos') ||
      q.includes('база') || q.includes('science'))
    return QUICK_RESPONSES['Наукові бази даних']

  if (q.includes('авторськ') || q.includes('orcid') || q.includes('профіл'))
    return QUICK_RESPONSES['Авторські профілі']

  if (q.includes('єдина картка') || q.includes('картка читача') || q.includes('каразін'))
    return QUICK_RESPONSES['Єдина картка читача']

  if (q.includes('структур') || q.includes('директор') || q.includes('відділ'))
    return QUICK_RESPONSES['Структура бібліотеки']

  if (q.includes('нові') || q.includes('надходжен') || q.includes('новинк'))
    return QUICK_RESPONSES['Нові надходження']

  return null
}
